#include <stdio.h>

int main() {
    int i = 1, j = 2, k;  // กำหนดค่าเริ่มต้น i = 1, j = 2, k ยังไม่กำหนด
    k = i + j;            // k = 1 + 2 = 3
    i = i + (k * j);      // i = 1 + (3 * 2) = 7
    j = i / 2;            // j = 7 / 2 = 3 (หารแบบจำนวนเต็ม)
    k = i % 2;            // k = 7 % 2 = 1 (หารเอาเศษ)
    i = (j + k) * 3;      // i = (3 + 1) * 3 = 12

    // แสดงผลลัพธ์
    printf("w06-02-01:\n");
    printf("i = %d, j = %d, k = %d\n", i, j, k);
    
    return 0;
} 


